package Codições;

public class Ternario {
    public static void main(String[] args) {

        /*
        * Ternario: são maneiras de reduzir o codigo;
        * vaeiavel = (condição) ? valorSeVerdadeiro : valorFalso;
        */

        short numeroDeMissoes = 9;
        String nivel = (numeroDeMissoes >= 10) ? "O ninja tem 10 ou mais missoes" : "O ninja tem menos de 10 missoes";
        System.out.println(nivel);





    }

}
